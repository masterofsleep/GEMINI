#' A Lib.pa Function
#' 
#' This functions allows you to load a bunch of packages with just one line
#' @param
#' @keywords library packages
#' @export
#' @examples 
#' lib.pa()

lib.pa <- function(){
  library(data.table)
  library(dplyr)
  library(plyr)
  library(ggplot2)
  library(stringr)
  library(xtable)
  library(readr)
  library(lubridate)
}
